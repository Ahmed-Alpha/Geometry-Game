import turtle


myturtle = turtle.Turtle()

myturtle.penup()
myturtle.goto(54,75)#Start at that coordinate

myturtle.pendown()
myturtle.forward(100)# Moves 100(amount) of pixels
myturtle.left(90)# Turns specific angle
myturtle.forward(200)
myturtle.left(90)
myturtle.forward(100)
myturtle.left(90)
myturtle.forward(200)

turtle.done()